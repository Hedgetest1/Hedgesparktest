"""
segment_monitor_worker.py — Proactive hot-segment action task + nudge creation.

Responsibility
--------------
Every cycle (default: 5 minutes), scan all active Pro shops for products
whose hot audience segment has crossed actionable thresholds.  When a
revenue window is open:
  1. Create (or dedup) a SCARCITY_NUDGE action task.
  2. Create (or refresh) an ActiveNudge — the storefront delivery artifact
     that spark-nudge.js polls to render real nudges on product pages.

This gives WishSpark a fully closed loop:
  behavioral data → empirical segment → task creation → live nudge delivery

Cycle
-----
  1. Query active Pro merchants (plan='pro' AND billing_active=true).
  2. For each Pro shop: find products with recent visitor activity from
     product_metrics (unique_visitors_24h >= MIN_ACTIVE_VISITORS_24H).
  3. For each active product: call segment_product_visitors() to compute the
     hot segment state for the last HOT_SEGMENT_WINDOW_HOURS.
  4. If hot.visitor_count >= MIN_HOT_VISITORS AND
        hot.estimated_revenue_window >= MIN_REVENUE_WINDOW:
       a. Check cooldown: skip task creation if a SCARCITY_NUDGE task was
          completed (done/failed/dismissed) within COOLDOWN_HOURS.
       b. Call create_task() — the executor's dedup handles the pending/
          executing case without a second check here.
       c. ALWAYS call create_or_refresh_nudge() when conditions are met —
          even on a task dedup hit.  This keeps the nudge TTL fresh and
          updates visitor_count/revenue_window to the latest scan data.
  5. Write a WorkerLog row for each cycle.
  6. Sleep SLEEP_SECONDS.

Deduplication strategy
-----------------------
Task dedup (two-layer):

  Active guard (handled by create_task):
    If a SCARCITY_NUDGE task for the same (shop, product, action_type) is
    already pending or executing, create_task() returns it unchanged.

  Cooldown guard (handled by _has_cooldown_task):
    If a SCARCITY_NUDGE task triggered by this worker was completed in the
    last COOLDOWN_HOURS, skip task creation entirely.

Nudge dedup (handled by create_or_refresh_nudge):
  If an active, non-expired nudge already exists for (shop, product,
  action_type), it is refreshed (expires_at extended, visitor_count updated)
  rather than replaced.  This ensures exactly one active nudge per product
  at all times while keeping the storefront copy current.

Nudge lifecycle
---------------
The nudge TTL (NUDGE_TTL_HOURS = 4) acts as a dead-man's switch:
  - While hot conditions persist: worker refreshes the nudge every 5 min,
    extending expires_at → nudge stays live indefinitely.
  - When conditions fall below threshold: worker stops refreshing.
    The nudge ages out in at most 4 hours without any explicit deletion.
  - The aggregation_worker sweeps expired nudges (status → 'expired')
    on every 5-minute cycle.

This means nudges are always self-healing:
  - No hot conditions → nudge naturally expires.
  - Hot conditions return → new nudge is created on the next cycle.
  - Hot conditions persist → nudge is continuously refreshed.

Action type selection
---------------------
SCARCITY_NUDGE is used for all hot-segment triggers:
  - Most lightweight intervention (social proof, interest signal).
  - Does not require discounting or inventory data.
  - Copy is fully truthful (grounded in real behavioral segment counts).

FLASH_INCENTIVE (discount code) is not auto-generated by this worker.
Discounting is a merchant decision requiring explicit approval.
"""
