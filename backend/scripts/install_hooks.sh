#!/usr/bin/env bash
# install_hooks.sh — wire preflight + TIER_0 auto-deploy hooks.
#
# Run once after cloning the repo. Safe to re-run — overwrites hooks.
#
# Installs:
#   pre-commit   → backend/scripts/preflight.sh (31 audits)
#   commit-msg   → backend/scripts/audit_commit_devils_advocate.py
#                  (blocks silent-deferral phrases in devil's advocate
#                   sections unless tagged Cat-4/5 per
#                   feedback_no_accettabile_per_beta.md)
#   post-commit  → backend/scripts/post_commit_auto_deploy.sh
#                  (TIER_0 auto-deploy per Phase 1.9.5)
#
# Usage:  bash backend/scripts/install_hooks.sh

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
PRE_HOOK="$REPO_ROOT/.git/hooks/pre-commit"
MSG_HOOK="$REPO_ROOT/.git/hooks/commit-msg"
POST_HOOK="$REPO_ROOT/.git/hooks/post-commit"
PREFLIGHT="backend/scripts/preflight.sh"
DA_AUDIT="backend/scripts/audit_commit_devils_advocate.py"
LATERAL_AUDIT="backend/scripts/audit_lateral_change_evidence.py"
UNRESOLVED_FLAGS_AUDIT="backend/scripts/audit_unresolved_flags.py"
DA_EVIDENCE_AUDIT="backend/scripts/audit_da_evidence.py"
NON_TRIVIAL_PROTOCOL_AUDIT="backend/scripts/audit_non_trivial_commit_protocol.py"
CAPILLARY_CLAIM_AUDIT="backend/scripts/audit_capillary_scope_claim.py"
TIER1_SURFACE_AUDIT="backend/scripts/audit_tier1_change_surfaced.py"
PROPAGATION_EVIDENCE_AUDIT="backend/scripts/audit_propagation_evidence.py"
AUTO_DEPLOY="backend/scripts/post_commit_auto_deploy.sh"

if [ ! -d "$REPO_ROOT/.git" ]; then
    echo "install_hooks: not a git repository root ($REPO_ROOT)" >&2
    exit 1
fi

chmod +x "$REPO_ROOT/$PREFLIGHT"
chmod +x "$REPO_ROOT/$AUTO_DEPLOY"
chmod +x "$REPO_ROOT/$DA_AUDIT"
chmod +x "$REPO_ROOT/$LATERAL_AUDIT"
chmod +x "$REPO_ROOT/$UNRESOLVED_FLAGS_AUDIT"
chmod +x "$REPO_ROOT/$DA_EVIDENCE_AUDIT"
chmod +x "$REPO_ROOT/$NON_TRIVIAL_PROTOCOL_AUDIT"
chmod +x "$REPO_ROOT/$CAPILLARY_CLAIM_AUDIT"
chmod +x "$REPO_ROOT/$TIER1_SURFACE_AUDIT"
chmod +x "$REPO_ROOT/$PROPAGATION_EVIDENCE_AUDIT"
chmod +x "$REPO_ROOT/backend/scripts/probe_capillary_scope.py"
chmod +x "$REPO_ROOT/backend/scripts/classify_commit_tier.py"

# --- pre-commit ---
cat > "$PRE_HOOK" <<EOF
#!/usr/bin/env bash
# Auto-generated by backend/scripts/install_hooks.sh
# Runs backend/scripts/preflight.sh before every commit.
# Bypass with \`git commit --no-verify\` if absolutely necessary.
exec "\$(git rev-parse --show-toplevel)/$PREFLIGHT"
EOF
chmod +x "$PRE_HOOK"

# --- commit-msg ---
# 2026-05-07 doctrine trim: 8/10 audits ADVISORY (warn-only). Only 2
# stay HARD-BLOCKING because they catch regressions worth blocking on:
#   1. audit_lateral_change_evidence — sibling thinking on remove/add
#   2. audit_tier1_change_surfaced   — TIER_1 audit-trail disclosure
# The other 8 surface analysis but exit 0 by default. Operator can
# pass --strict to any single audit for a periodic compliance check.
# Rationale: doctrine compounding made local commits 6-8 min each
# (founder feedback "perchè le sessioni sono divenute cosi tanto più
# lunghe"). High-value gates kept; ritual gates advisory.
cat > "$MSG_HOOK" <<EOF
#!/usr/bin/env bash
# Auto-generated by backend/scripts/install_hooks.sh
# \$1 = path to the commit message file git is about to use.
# 2026-05-07 doctrine trim — see install_hooks.sh for rationale.
set -e
ROOT="\$(git rev-parse --show-toplevel)"

# ── HARD GATES (always block on fail) ──
"\$ROOT/$LATERAL_AUDIT" --msg-file "\$1"
"\$ROOT/$TIER1_SURFACE_AUDIT" --msg-file "\$1"

# ── ADVISORY (analysis printed; exit 0 unless --strict) ──
"\$ROOT/$DA_AUDIT" "\$1"
"\$ROOT/$UNRESOLVED_FLAGS_AUDIT" --text-file "\$1" --lenient
"\$ROOT/$DA_EVIDENCE_AUDIT" --text-file "\$1"
"\$ROOT/$NON_TRIVIAL_PROTOCOL_AUDIT" --msg-file "\$1" --lenient
"\$ROOT/$CAPILLARY_CLAIM_AUDIT" --commit-msg-file "\$1"
"\$ROOT/$PROPAGATION_EVIDENCE_AUDIT" --msg-file "\$1" --lenient
"\$ROOT/backend/scripts/audit_close_claim_evidence.py" "\$1"
"\$ROOT/backend/scripts/audit_critical_alert_coverage.py" "\$1"
EOF
chmod +x "$MSG_HOOK"

# --- post-commit ---
cat > "$POST_HOOK" <<EOF
#!/usr/bin/env bash
# Auto-generated by backend/scripts/install_hooks.sh
# Runs backend/scripts/post_commit_auto_deploy.sh after every commit.
# Classifies the commit (TIER_0/1/2) and auto-deploys only TIER_0.
# Opt-out per-commit: HS_NO_AUTO_DEPLOY=1 git commit -m "..."
exec "\$(git rev-parse --show-toplevel)/$AUTO_DEPLOY"
EOF
chmod +x "$POST_HOOK"

echo "install_hooks: pre-commit  → $PREFLIGHT"
echo "install_hooks: commit-msg  → $DA_AUDIT (devil's advocate enforcement)"
echo "install_hooks: post-commit → $AUTO_DEPLOY (TIER_0 auto-deploy)"
